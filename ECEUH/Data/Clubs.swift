extension Club {
    /// Compact initializer for the directory data above.
    init(_ slug: String, _ name: String, _ acronym: String?, _ category: ClubCategory,
         email: String? = nil, web: String? = nil, tags: [String], featured: Bool = false) {
        self.init(slug: slug, name: name, acronym: acronym, category: category,
                  email: email, website: web, tags: tags, featured: featured)
    }
}

func clubBySlug(_ slug: String) -> Club? {
    kClubs.first { $0.slug == slug }
}
